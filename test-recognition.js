const fs = require('fs');

// Create a simple test image (2x2 pixels, red)
const testImage = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG header
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
  0x00, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x02, // 2x2 pixels
  0x08, 0x02, 0x00, 0x00, 0x00, 0xFD, 0x52, 0x93, // bit depth, color type, etc.
  0x8A, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, // IDAT chunk
  0x54, 0x08, 0x99, 0x01, 0x01, 0x00, 0x00, 0xFF, // compressed data
  0xFF, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01, 0xE2, // more data
  0x21, 0xBC, 0x33, 0x00, 0x00, 0x00, 0x00, 0x49, // IEND chunk
  0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
]);

const base64Image = testImage.toString('base64');

async function testRecognition() {
  try {
    console.log('Testing recognition API...');
    
    const response = await fetch('http://localhost:3000/api/recognize-v3', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        imageBase64: base64Image,
        enableAIRouter: true,
        aiBudget: 0.05,
        aiPriority: 'accuracy'
      })
    });

    const result = await response.json();
    console.log('Response:', JSON.stringify(result, null, 2));
    
  } catch (error) {
    console.error('Error:', error);
  }
}

testRecognition();
